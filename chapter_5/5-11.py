# Ordinal numbers

num_list = range(1,10)

for i in range(len(num_list)):
    if num_list[i] == 1:
        print(f"\n{num_list[i]}st")
    
    elif num_list[i] == 2:
        print(f"\n{num_list[i]}nd")
    
    elif num_list[i] == 3:
        print(f"\n{num_list[i]}rd")
    
    else:
        print(f"\n{num_list[i]}th")
        
print("\n")
        
    
        

        
        